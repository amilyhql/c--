#include <stdio.h>
int main(void)
{
	int a,b;
	double m,n;
	printf("请输入两个实数:\n"); scanf("%d%d",&a,&b);
	printf("请输入两个浮点数：\n"); scanf("%lf%lf",&m,&n);
	printf("a+b=%d\n",a+b);
	printf("a-b=%d\n",a-b);
	printf("a*b=%d\n",a*b);
	printf("a/b=%d\n",a/b);
	printf("m+n=%f\n",m+n);
	printf("m-n=%f\n",m-n);
	printf("m*n=%f\n",m-n);
	printf("m/n=%f\n",m/n);
	return 0;
 } 